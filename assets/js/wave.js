function buildWave(w, h) {

  var m = 0.512286623256592433;

  var pathData = [
    'M', 0, h * 0.625,
    'c',  0.25 * h * m, 0,
    0.25 * h * (1 - m), -0.25 * h,
    0.25 * h, -0.25 * h,
    's', 0.25 * h * (1 - m), 0.25 * h,
    0.25 * h, 0.25 * h,
    's', 0.25 * h * (1 - m), -0.25 * h,
    0.25 * h, -0.25 * h,
    's', 0.25 * h * (1 - m), 0.25 * h,
    0.25 * h, 0.25 * h,
    's', 0.25 * h * (1 - m), -0.25 * h,
    0.25 * h, -0.25 * h,
    's', 0.25 * h * (1 - m), 0.25 * h,
    0.25 * h, 0.25 * h,
    's', 0.25 * h * (1 - m), -0.25 * h,
    0.25 * h, -0.25 * h,
    's', 0.25 * h * (1 - m), 0.25 * h,
    0.25 * h, 0.25 * h,
    's', 0.25 * h * (1 - m), -0.25 * h,
    0.25 * h, -0.25 * h,
    's', 0.25 * h * (1 - m), 0.25 * h,
    0.25 * h, 0.25 * h,
    's', 0.25 * h * (1 - m), -0.25 * h,
    0.25 * h, -0.25 * h,
    's', 0.25 * h * (1 - m), 0.25 * h,
    0.25 * h, 0.25 * h,
  ].join(' ');
  longWave.setAttribute('d', pathData);
}

var longWave = document.querySelector('#long-wave');
buildWave(90, 60);
